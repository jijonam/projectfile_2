package vo;

public class LectureCartVo {
	
	
	String lecture_name;
	String lecture_teacher;
	String lecture_price;
	String lecture_discount_amount;
	String lecture_payment_amount;
	
	public String getLecture_name() {
		return lecture_name;
	}
	public void setLecture_name(String lecture_name) {
		this.lecture_name = lecture_name;
	}
	public String getLecture_teacher() {
		return lecture_teacher;
	}
	public void setLecture_teacher(String lecture_teacher) {
		this.lecture_teacher = lecture_teacher;
	}
	public String getLecture_price() {
		return lecture_price;
	}
	public void setLecture_price(String lecture_price) {
		this.lecture_price = lecture_price;
	}
	public String getLecture_discount_amount() {
		return lecture_discount_amount;
	}
	public void setLecture_discount_amount(String lecture_discount_amount) {
		this.lecture_discount_amount = lecture_discount_amount;
	}
	public String getLecture_payment_amount() {
		return lecture_payment_amount;
	}
	public void setLecture_payment_amount(String lecture_payment_amount) {
		this.lecture_payment_amount = lecture_payment_amount;
	}
	
	
	

}
